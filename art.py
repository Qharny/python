import turtle as T

T.pensize(2)
T.bgcolor('lavender')
T.forward(100)  # Move the turtle forward by 100 units
T.right(90)     # Turn the turtle right by 90 degrees
T.forward(100)
T.right(90)
T.forward(100)
T.right(90)
T.forward(100)
T.fillcolor('blue')


# Close the T graphics window when done
T.done()